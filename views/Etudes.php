  		<h2>Quand j'étais petit</h2>
  		Lorsque j'étais enfant, j'aimais particulièrement jouer avec des Legos Technic, une variante des célèbres petites briques de couleurs, qui permettaient de construire n'importe quel véhicule pièce par pièce avec des mouvements engendrés par des mécanismes.<br><br>
  		Partant de ces constructions, intrigué par la complexité des mécanismes qui composent nos moyens de transport modernes et fasciné par l'aviation plus que n'importe quel autre transport, je voulais dans un premier temps devenir ingénieur en aéronautique.<br><br>
  		Je voulais créer, inventer, imaginer l'avion du futur, plus performant, plus écologique, plus moderne.
  		<span class="ancres" id="programme"></span>
  		<h2>Pourquoi l'informatique alors?</h2>
  		Arrivé au lycée, dès la seconde et de nouveau en Terminale, j'ai pu choisir comme option ISN pour Informatique Science du Numérique.
  		Avec cette option, j'ai découvert, parfois seulement survolé, des langages de mise en page et programmation tels que :
  		<ul>
  			<li>HTML</li>
  			<li>CSS</li>
  			<li>PHP</li>
  			<li>MySQL</li>
  			<li>Python</li>
  			<li>JavaScript</li>
  		</ul>
  		Après avoir réalisé plusieurs projets, surtout en JavaScript, dont un pour le baccalauréat, j'ai commencé à vraiment apprécier la programmation.<br><br>
  		De la même manière que les Legos, chaque ligne de code représente une pièce, qui une fois toutes assemblées, forment l'édifice qu'est le programme. Ce que j'aime aussi, c'est de pouvoir voir le programme évoler à chaque ligne ajoutée, ce qui permet de bien mieux suivre l'avancement et permet de faire des tests, c'est vraiment très pratique.<br><br>
  		Voyant une meilleure opportunité pour moi dans un milieu en évolution constante avec le développement rapide des technologies, je me suis tourné vers des études d'informatique.
  		<span class="ancres" id="conclusion"></span>
  		<h2>Le futur que je me souhaite</h2>
